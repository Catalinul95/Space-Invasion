class Entity {
    setPosX(x) {
        this.x = x;
    }
    getPosX() {
        return this.x;
    }
    setPosY(y) {
        this.y = y;
    }
    getPosY() {
        return this.y;
    }
    setWidth(width) {
        this.width = width;
    }
    getWidth() {
        return this.width;
    }
    setHeight(height) {
        this.height = height;
    }
    getHeight() {
        return this.height;
    }
    setSprite(sprite) {
        this.sprite = sprite;
    }
    getSprite() {
        return this.sprite;
    }
    setVelocity(velocity) {
        this.velocity = velocity;
    }
    getVelocity() {
        return this.velocity;
    }
}